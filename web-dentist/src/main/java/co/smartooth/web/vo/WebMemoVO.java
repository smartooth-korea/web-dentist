package co.smartooth.web.vo;


public class WebMemoVO {
	
	private String memoCd;
	private String memoDepth;
	private String memoDesription;
	
	
	
	
	public String getMemoCd() {
		return memoCd;
	}
	public void setMemoCd(String memoCd) {
		this.memoCd = memoCd;
	}
	public String getMemoDepth() {
		return memoDepth;
	}
	public void setMemoDepth(String memoDepth) {
		this.memoDepth = memoDepth;
	}
	public String getMemoDesription() {
		return memoDesription;
	}
	public void setMemoDesription(String memoDesription) {
		this.memoDesription = memoDesription;
	}
	
	
}
